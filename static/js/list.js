//1.折叠筛选框
new Vue({
    el:"#filter-box",
    data:{
        isToggled_1:false,
        isToggled_2:false
    },
    methods:{
        toggle_1(){
            this.isToggled_1=!this.isToggled_1;
        },
        toggle_2(){
            this.isToggled_2=!this.isToggled_2;
        }
    }
})
//2.加载商品数据
$(function(){
    $.ajax({
        url:"http://127.0.0.1:90/list",
        type:"get",
        dataType:"json",
        success:function(res){
            console.log(res);
            new Vue({
                el:"#goods",
                data:{res},
                methods:{
                    //大小图切换
                    changeImg(i,index){
                        this.res[index].current=this.res[index].imgs[i];
                    }
                }
            })
        }
    })
})